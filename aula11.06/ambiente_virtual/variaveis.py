# variaveis são usadas para salvar algo na memoria do computador
# o sinal de = é ultilizado para atribuições de valores
var01 = "alguma coisa"
print(var01)
var01 = "outra coisa"
print(var01)
var01 = 10
print(var01)
var02 = "a " + "b " + "c"
print(var02)
var02 = "A" * 10
print(var02)
var02 = 10 * "Felipe "
print(var02, var01, sep="/")
